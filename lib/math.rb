def addition(num1, num2)
  sum = num1 + num2
  puts sum
  sum
end

def subtraction(num1, num2)
  difference = num1 - num2
  puts difference
  difference
end

def division(num1, num2)
  quotient = num1 / num2
  puts quotient
  quotient
end

def multiplication(num1, num2)
  multiple = num1 * num2
  puts multiple
  multiple
end

def modulo(num1, num2)
  remainder = num1.modulo(num2)
  puts remainder
  remainder
end

def square_root(num)
  root = Math.sqrt(num)
  puts root
  root
end